package helpers;

public class Printing {

    public static void print(boolean parameter) {
        System.out.println(parameter);
    }

    public static void print(boolean[] parameter) {
        System.out.println(parameter);
    }

    public static void print(String parameter) {
        System.out.println(parameter);
    }

    public static void print(String[] parameter) {
        System.out.println(parameter);
    }

    public static void print(int parameter) {
        System.out.println(parameter);
    }

    public static void print(int[] parameter) {
        System.out.println(parameter);
    }

}
